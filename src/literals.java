public class literals {
    public static void main(String[] args) {
        byte age = 54;
        char name='y';
        float deci=54.5f;
        long longint= 555452725;
        double doubledeci=5410.32d;
        boolean bootf=false;
        String speech = "My name is Yashpal Bishnoi";
        System.out.println(age);
        System.out.println(name);
        System.out.println(deci);
        System.out.println(doubledeci);
        System.out.println(longint);
        System.out.println(bootf);
        System.out.println(speech);
    }
}

